export const getModels = (filters = {}) => {
  let filtered = [...mockModels];
  
  if (filters.category && filters.category !== 'Todos') {
    filtered = filtered.filter(m => m.category === filters.category);
  }
  
  if (filters.verified) {
    filtered = filtered.filter(m => m.verified);
  }
  
  if (filters.openSchedule) {
    filtered = filtered.filter(m => m.badges.includes('Agenda aberta') || m.status === 'Disponível hoje');
  }
  
  if (filters.priceRange) {
    filtered = filtered.filter(m => m.price >= filters.priceRange[0] && m.price <= filters.priceRange[1]);
  }
  
  return filtered;
};

